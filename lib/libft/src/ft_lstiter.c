#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (lst && f)
	{
		while (lst)
		{
			f(lst->content);
			lst = lst->next;
		}
	}
	else
		ft_errmsg("[ft_lstclear] lst or del functions is/are NULL\n");
}
