#include <libft.h>

void	*ft_memset(void *b, int c, size_t len)
{
	char	*b_cpy;

	b_cpy = b;
	if (b)
	{
		while (len--)
			*b_cpy++ = c;
	}
	else
		ft_errmsg("[ft_memset] buffer to set is a NULL");
	return (b);
}
